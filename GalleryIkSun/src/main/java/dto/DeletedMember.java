package dto;

//탈퇴 회원 정보
public class DeletedMember {
	private int num;
	private String deletedName; 			// 탈퇴 회원 이름
	private String deletedId;				// 탈퇴 회원 아이디
	private String deletedEmail;			// 탈퇴 회원 이메일
	private String deletedPhone; 			// 탈퇴 회원 전화번호
	private String deletedAddress; 			// 탈퇴 회원 주소
	private String deletedAddress2; 		// 탈퇴 회원 상세주소
	private String deletedPost; 			// 탈퇴 회원 우편번호
	private String deletedBank; 			// 탈퇴 회원 은행
	private String deletedAccountNumber; 	// 탈퇴 회원 계좌번호
	private String deletedAccountHandler;   // 탈퇴 회원 계좌번호 예금주
	private String deletedUserType; 		// 탈퇴 회원 회원 등급 
	private String deletedAt; 				// 탈퇴 시간
	private String deletedReason;			// 탈퇴 이유
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getDeletedName() {
		return deletedName;
	}
	public void setDeletedName(String deletedName) {
		this.deletedName = deletedName;
	}
	public String getDeletedId() {
		return deletedId;
	}
	public void setDeletedId(String deletedId) {
		this.deletedId = deletedId;
	}
	public String getDeletedEmail() {
		return deletedEmail;
	}
	public void setDeletedEmail(String deletedEmail) {
		this.deletedEmail = deletedEmail;
	}
	public String getDeletedPhone() {
		return deletedPhone;
	}
	public void setDeletedPhone(String deletedPhone) {
		this.deletedPhone = deletedPhone;
	}
	public String getDeletedAddress() {
		return deletedAddress;
	}
	public void setDeletedAddress(String deletedAddress) {
		this.deletedAddress = deletedAddress;
	}
	public String getDeletedAddress2() {
		return deletedAddress2;
	}
	public void setDeletedAddress2(String deletedAddress2) {
		this.deletedAddress2 = deletedAddress2;
	}
	public String getDeletedPost() {
		return deletedPost;
	}
	public void setDeletedPost(String deletedPost) {
		this.deletedPost = deletedPost;
	}
	public String getDeletedBank() {
		return deletedBank;
	}
	public void setDeletedBank(String deletedBank) {
		this.deletedBank = deletedBank;
	}
	public String getDeletedAccountNumber() {
		return deletedAccountNumber;
	}
	public void setDeletedAccountNumber(String deletedAccountNumber) {
		this.deletedAccountNumber = deletedAccountNumber;
	}
	public String getDeletedAccountHandler() {
		return deletedAccountHandler;
	}
	public void setDeletedAccountHandler(String deletedAccountHandler) {
		this.deletedAccountHandler = deletedAccountHandler;
	}
	public String getDeletedUserType() {
		return deletedUserType;
	}
	public void setDeletedUserType(String deletedUserType) {
		this.deletedUserType = deletedUserType;
	}
	public String getDeletedAt() {
		return deletedAt;
	}
	public void setDeletedAt(String deletedAt) {
		this.deletedAt = deletedAt;
	}
	public String getDeletedReason() {
		return deletedReason;
	}
	public void setDeletedReason(String deletedReason) {
		this.deletedReason = deletedReason;
	}
	
	
}