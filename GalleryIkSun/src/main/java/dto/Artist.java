package dto;

public class Artist {
	private int num;
	private String artistName;
	private String artistPicture;
	private String artistPhone;
	private String artistEmail;
	private String artistAddress;
	private String artistCreatedAt;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getArtistName() {
		return artistName;
	}
	public void setArtistName(String artistName) {
		this.artistName = artistName;
	}
	public String getArtistPicture() {
		return artistPicture;
	}
	public void setArtistPicture(String artistPicture) {
		this.artistPicture = artistPicture;
	}
	public String getArtistPhone() {
		return artistPhone;
	}
	public void setArtistPhone(String artistPhone) {
		this.artistPhone = artistPhone;
	}
	public String getArtistEmail() {
		return artistEmail;
	}
	public void setArtistEmail(String artistEmail) {
		this.artistEmail = artistEmail;
	}
	public String getArtistAddress() {
		return artistAddress;
	}
	public void setArtistAddress(String artistAddress) {
		this.artistAddress = artistAddress;
	}
	public String getArtistCreatedAt() {
		return artistCreatedAt;
	}
	public void setArtistCreatedAt(String artistCreatedAt) {
		this.artistCreatedAt = artistCreatedAt;
	}
	
}
